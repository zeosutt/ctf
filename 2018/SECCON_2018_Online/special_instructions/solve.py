x = 0x92d68ca2

def xor32():
	global x
	x = (x ^ x << 13) & 0xffffffff
	x = (x ^ x >> 17) & 0xffffffff
	x = (x ^ x << 15) & 0xffffffff
	return x

flag = '\x6d\x72\xc3\xe2\xcf\x95\x54\x9d\xb6\xac\x03\x84\xc3\xc2\x35\x93\xc3\xd7\x7c\xe2\xdd\xd4\xac\x5e\x99\xc9\xa5\x34\xde\x06\x4e'
randval = '\x3d\x05\xdc\x31\xd1\x8a\xaf\x29\x96\xfa\xcb\x1b\x01\xec\xe2\xf7\x15\x70\x6c\xf4\x7e\xa1\x9e\x0e\x01\xf9\xc2\x4c\xba\xa0\xa1'

ans = ''
for ch1, ch2 in zip(flag, randval):
	ans += chr(ord(ch1) ^ ord(ch2) ^ xor32() & 0xff)

print(ans)
