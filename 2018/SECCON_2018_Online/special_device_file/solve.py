x = 0x139408dcbbf7a44

def xor64():
	global x
	x = (x ^ x << 13) & 0xffffffffffffffff
	x = (x ^ x >> 7) & 0xffffffffffffffff
	x = (x ^ x << 17) & 0xffffffffffffffff
	return x

flag = '\xfe\x75\x88\xa9\x5a\xaa\x10\x52\x9c\x6a\x67\xf4\x82\xbe\x21\x56\x59\x0b\x97\x32\x21\x46\x93\xae\x40\x0d\x2e\x1f\x83\x43\x40'
randval = '\x1d\xab\x1b\x0f\xa7\xd9\x1a\xb0\x61\x7e\xb6\x48\xa4\x56\xcf\x7e\x49\x05\xfd\x05\x9c\xf9\x54\x45\xfa\x24\xc6\x1d\x68\xf2\x46'

ans = ''
for ch1, ch2 in zip(flag, randval):
	ans += chr(ord(ch1) ^ ord(ch2) ^ xor64() & 0xff)

print(ans)
